import android.icu.text.Transliterator

object ChineseTextConverter {
    private val traditionalToSimplifiedTransliterator = runCatching {
        Transliterator.getInstance("Traditional-Simplified")
    }.getOrNull()

    private val simplifiedToTraditionalTransliterator = runCatching {
        Transliterator.getInstance("Simplified-Traditional")
    }.getOrNull()

    fun canConvertLocally(source: TranslationLanguage, target: TranslationLanguage): Boolean =
        (source == TranslationLanguage.TraditionalChinese && target == TranslationLanguage.SimplifiedChinese) ||
            (source == TranslationLanguage.SimplifiedChinese && target == TranslationLanguage.TraditionalChinese) ||
            source == target

    fun convert(text: String, source: TranslationLanguage, target: TranslationLanguage): String =
        when {
            source == target -> text.trim()
            source == TranslationLanguage.TraditionalChinese && target == TranslationLanguage.SimplifiedChinese ->
                traditionalToSimplified(text)
            source == TranslationLanguage.SimplifiedChinese && target == TranslationLanguage.TraditionalChinese ->
                simplifiedToTraditional(text)
            else -> ""
        }

    fun traditionalToSimplified(text: String): String {
        val normalized = text.trim()
        if (normalized.isBlank()) {
            return ""
        }

        return traditionalToSimplifiedTransliterator
            ?.transliterate(normalized)
            ?: fallbackTraditionalToSimplified(normalized)
    }

    fun simplifiedToTraditional(text: String): String {
        val normalized = text.trim()
        if (normalized.isBlank()) {
            return ""
        }

        return simplifiedToTraditionalTransliterator
            ?.transliterate(normalized)
            ?: fallbackSimplifiedToTraditional(normalized)
    }

    private fun fallbackTraditionalToSimplified(text: String): String {
        val builder = StringBuilder(text.length)
        text.forEach { char ->
            builder.append(fallbackMap[char] ?: char)
        }
        return builder.toString()
    }

    private fun fallbackSimplifiedToTraditional(text: String): String {
        val reverseMap = fallbackMap.entries.associate { (traditional, simplified) -> simplified to traditional }
        val builder = StringBuilder(text.length)
        text.forEach { char ->
            builder.append(reverseMap[char] ?: char)
        }
        return builder.toString()
    }

